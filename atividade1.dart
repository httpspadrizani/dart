void main() {
  int nota1 = 9;
  int nota2 = 8;
  int nota3 = 7;
  int nota4 = 8;

  double media = (nota1 + nota2 + nota3 + nota4) / 4;

  // Usando um loop for para verificar se a média é maior ou igual a 7
  for (int i = 0; i < 1; i++) {
    if (media >= 7) {
      print("Você foi aprovado");
    }
  }
}
